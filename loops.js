/*to print even numbers between 0-12*/
let even = 0;
while (even < 12) {
  console.log(even);
  even = even + 2;
}
/*do while loop*/
let yourName;
do {
  yourName = prompt("What is your name");
} while (!yourName);
console.log(yourName);
/*for loop */
for (let number = 0; number < 12; number = 0 + 2) {
  console.log(number);
}
